export function exportToMarkdown(): string {
  const { header, contact, summary, personalSkills, languages, experience, education, certifications, skills, interests } = cvData;
  
  let markdown = `# ${header.name}\n`;
  markdown += `## ${header.title}\n\n`;
  markdown += `*${header.tagline}*\n\n`;
  
  markdown += `## Contact Information\n`;
  markdown += `- **Email:** ${contact.email}\n`;
  markdown += `- **Phone:** ${contact.phone}\n`;
  markdown += `- **Location:** ${contact.location}\n`;
  markdown += `- **Website:** ${contact.website}\n`;
  markdown += `- **GitHub:** github.com/${contact.github}\n\n`;
  
  markdown += `## Summary\n${summary}\n\n`;
  
  markdown += `## Personal Skills\n`;
  personalSkills.forEach(skill => {
    markdown += `- ${skill}\n`;
  });
  markdown += `\n`;
  
  markdown += `## Languages\n`;
  languages.forEach(lang => {
    markdown += `- **${lang.name}:** ${lang.level}\n`;
  });
  markdown += `\n`;
  
  markdown += `## Work Experience\n`;
  experience.forEach(exp => {
    markdown += `### ${exp.title}\n`;
    markdown += `**${exp.company}** | ${exp.period}\n`;
    if (exp.websiteUrl) {
      markdown += `*Website: ${exp.websiteUrl}*\n`;
    }
    markdown += `\n`;
    exp.description.forEach(desc => {
      markdown += `- ${desc}\n`;
    });
    markdown += `\n`;
  });
  
  markdown += `## Education\n`;
  education.forEach(edu => {
    markdown += `### ${edu.degree}\n`;
    markdown += `**${edu.institution}** | ${edu.period}\n`;
    markdown += `${edu.description}\n\n`;
  });
  
  markdown += `## Certifications\n`;
  certifications.forEach(cert => {
    markdown += `- ${cert}\n`;
  });
  markdown += `\n`;
  
  markdown += `## Technical Skills\n`;
  Object.entries(skills).forEach(([category, skillList]) => {
    markdown += `### ${category}\n`;
    markdown += skillList.join(', ') + '\n\n';
  });
  
  markdown += `## Interests & Specializations\n`;
  interests.forEach(interest => {
    markdown += `- ${interest}\n`;
  });
  
  return markdown;
}